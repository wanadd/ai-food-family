def ing(
    display: str,
    amount: float | int | str,
    unit: str,
    slug: str | None = None,
    shop: str | None = None,
    pantry: str | None = None,
    **extra: Any,
) -> dict[str, Any]:
    canonical = display.lower()
    return {
        "display_name": display,
        "canonical_name": canonical,
        "canonical_slug": slug or canonical.replace(" ", "_").replace("-", "_"),
        "amount": amount,
        "unit": unit,
        **({"shopping_category_slug": shop} if shop else {}),
        **({"pantry_category_slug": pantry} if pantry else {}),
        **extra,
    }


def step(order: int, instruction: str, title: str | None = None, minutes: int | None = None) -> dict:
    row: dict[str, Any] = {"order": order, "title": title or f"Шаг {order}", "instruction": instruction}
    if minutes is not None:
        row["duration_minutes"] = minutes
    return row


def recipe(
    title: str,
    meal_types: list[str],
    category: str,
    prep: int,
    cook: int,
    servings: int,
    ingredients: list[dict],
    steps: list[dict],
    kcal: float,
    protein: float,
    fat: float,
    carbs: float,
    *,
    diet_tags: list[str] | None = None,
    excludes: list[str] | None = None,
    religious_tags: list[str] | None = None,
    allergens: list[str] | None = None,
    tags: list[str] | None = None,
    description: str = "",
    difficulty: str = "easy",
) -> dict[str, Any]:
    return {
        "title": title,
        "description": description,
        "meal_types": meal_types,
        "category": category,
        "servings": servings,
        "prep_time_minutes": prep,
        "cook_time_minutes": cook,
        "total_time_minutes": prep + cook,
        "difficulty": difficulty,
        "source_type": "seed",
        "recipe_schema_version": 2,
        "status": "gold",
        "nutrition_summary": {
            "calories": kcal,
            "protein_g": protein,
            "fat_g": fat,
            "carbs_g": carbs,
            "fiber_g": 3,
            "confidence": "estimated",
        },
        "ingredients": ingredients,
        "steps": steps,
        "diet_tags": diet_tags or [],
        "excludes": excludes or [],
        "religious_tags": religious_tags or [],
        "allergens": allergens or [],
        "tags": tags or ["gold_v2"],
    }


