def main() -> int:
    out_path = Path(__file__).with_name("gold_recipes_30.jsonl")
    invalid = 0
    with out_path.open("w", encoding="utf-8") as fh:
        for raw in RECIPES:
            result = validate_recipe_v2(raw)
            if not result["valid"]:
                invalid += 1
                print(f"INVALID: {raw['title']}: {result['errors']}", file=sys.stderr)
            fh.write(json.dumps(result["normalized_recipe"], ensure_ascii=False) + "\n")

    print(f"Wrote {len(RECIPES)} recipes to {out_path} ({invalid} invalid)")
    return 1 if invalid else 0


if __name__ == "__main__":
    raise SystemExit(main())
