export function normalizePantryMatchKey(name: string): string {
  let key = name
    .trim()
    .toLowerCase()
    .replace(/ё/g, "е")
    .replace(/\s+/g, " ");
  return PANTRY_SYNONYMS[key] ?? key;
}
