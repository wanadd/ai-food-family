def to_canonical_slug(category: str) -> str:
    if category in NON_FOOD_CATEGORIES:
        return category
    return CANONICAL_SLUG_MAP.get(category, category)


def normalize_category(raw: str | None) -> str:
    if not raw:
        return "продукты"
    key = raw.strip().lower().replace(" ", "_")
    if key in CATEGORY_ALIASES:
        return CATEGORY_ALIASES[key]
    if key in CATEGORY_ORDER:
        return to_canonical_slug(key)
    if key in NON_FOOD_CATEGORIES:
        return key
    return "продукты"


def infer_category(name: str, hint: str | None) -> str:
    if hint:
        category = normalize_category(hint)
        if category in NON_FOOD_CATEGORIES:
            return category
        if category != "продукты":
            return category
    lowered = name.lower()
    for cat, keywords in CATEGORY_KEYWORDS:
        if any(word in lowered for word in keywords):
            return to_canonical_slug(cat)
    return "продукты"


def is_food_category(category: str) -> bool:
    return normalize_category(category) not in NON_FOOD_CATEGORIES
