        ("Мука пшеничная", "бакалея"),
        ("Мука", "бакалея"),
        ("Сахар", "бакалея"),
        ("Соль", "бакалея"),
        ("Масло растительное", "бакалея"),
        ("Паприка", "специи_соусы"),
