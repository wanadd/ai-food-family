        ("Картофель", "овощи_зелень"),
        ("Морковь", "овощи_зелень"),
        ("Помидоры", "овощи_зелень"),
        ("Огурец", "овощи_зелень"),
        ("Чеснок", "овощи_зелень"),
        ("Яблоки", "фрукты"),
        ("Банан", "фрукты"),
        ("Молоко 2.5%", "молочные"),
        ("Творог 5%", "молочные"),
        ("Сыр твёрдый", "молочные"),
        ("Яйца С1", "яйца"),
        ("Яйцо", "яйца"),
        ("Яйцо куриное", "яйца"),
        ("Рис", "крупы_макароны"),
        ("Гречка", "крупы_макароны"),
        ("Макароны", "крупы_макароны"),
        ("Булгур", "крупы_макароны"),
